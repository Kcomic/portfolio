// One-off generator for metric-adjusted fallback @font-face rules (capsize).
// Run: node scripts/gen-font-fallbacks.mjs > src/styles/font-fallbacks.css
// Produces a fallback face per Latin family whose ascent/descent/line-gap/
// width are scaled to match the real font, so swapping the real font in causes
// no layout shift. Thai keeps its own face (its metrics already match closely).
import { createFontStack } from "@capsizecss/core";
import spaceGrotesk from "@capsizecss/metrics/spaceGrotesk";
import ibmPlexSans from "@capsizecss/metrics/ibmPlexSans";
import ibmPlexMono from "@capsizecss/metrics/ibmPlexMono";
import arial from "@capsizecss/metrics/arial";
import courierNew from "@capsizecss/metrics/courierNew";

const pairs = [
  { target: spaceGrotesk, fallbacks: [arial] },
  { target: ibmPlexSans, fallbacks: [arial] },
  { target: ibmPlexMono, fallbacks: [courierNew] },
];

let css = "/* Generated by scripts/gen-font-fallbacks.mjs — do not edit by hand. */\n";
const families = [];
for (const { target, fallbacks } of pairs) {
  const { fontFamily, fontFaces } = createFontStack([target, ...fallbacks]);
  families.push({ name: target.familyName, stack: fontFamily });
  css += `\n/* ${target.familyName} */\n${fontFaces}\n`;
}

console.error(JSON.stringify(families, null, 2));
console.log(css);
